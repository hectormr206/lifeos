# Detect system architecture
detect_arch() {
    local arch=$(uname -m)
    case "$arch" in
        x86_64)
            echo "amd64"
            ;;
        aarch64|arm64)
            echo "arm64"
            ;;
        *)
            log_error "Unsupported architecture: $arch"
            exit 1
            ;;
    esac
}

# Detect GPU and return configuration
detect_gpu() {
    local gpu_info=""
    local gpu_type="none"
    local vram_mb=0
    
    # Check for NVIDIA GPU
    if command -v nvidia-smi &> /dev/null; then
        local nvidia_output=$(nvidia-smi --query-gpu=name,memory.total --format=csv,noheader,nounits 2>/dev/null || true)
        if [ -n "$nvidia_output" ]; then
            gpu_type="nvidia"
            local gpu_name=$(echo "$nvidia_output" | head -1 | cut -d',' -f1 | xargs)
            vram_mb=$(echo "$nvidia_output" | head -1 | cut -d',' -f2 | xargs | tr -d ' MiB')
            gpu_info="NVIDIA $gpu_name (${vram_mb}MB VRAM)"
            log_success "Detected GPU: $gpu_info"
        fi
    fi
    
    # Check for AMD GPU via rocminfo or lspci
    if [ "$gpu_type" = "none" ]; then
        if command -v rocminfo &> /dev/null; then
            local amd_gpu=$(rocminfo 2>/dev/null | grep "Marketing Name" | head -1 | cut -d':' -f2 | xargs || true)
            if [ -n "$amd_gpu" ]; then
                gpu_type="amd"
                vram_mb=16384 # Default estimate for AMD
                gpu_info="AMD $amd_gpu"
                log_success "Detected GPU: $gpu_info"
            fi
        elif command -v lspci &> /dev/null; then
            local amd_lspci=$(lspci 2>/dev/null | grep -i "vga.*amd" | head -1 || true)
            if [ -n "$amd_lspci" ]; then
                gpu_type="amd"
                vram_mb=8192
                gpu_info="AMD GPU (via lspci)"
                log_success "Detected GPU: $gpu_info"
            fi
        fi
    fi
    
    # Check for Intel GPU via intel-gpu-tools or lspci
    if [ "$gpu_type" = "none" ]; then
        if command -v intel_gpu_top &> /dev/null; then
            gpu_type="intel"
            vram_mb=4096 # Intel integrated/shared memory
            gpu_info="Intel GPU"
            log_success "Detected GPU: $gpu_info"
        elif command -v lspci &> /dev/null; then
            local intel_lspci=$(lspci 2>/dev/null | grep -i "vga.*intel" | head -1 || true)
            if [ -n "$intel_lspci" ]; then
                gpu_type="intel"
                vram_mb=4096
                gpu_info="Intel GPU (via lspci)"
                log_success "Detected GPU: $gpu_info"
            fi
        fi
    fi
    
    if [ "$gpu_type" = "none" ]; then
        log_warn "No GPU detected - will use CPU mode"
        gpu_info="CPU only"
    fi
    
    echo "$gpu_type:$vram_mb:$gpu_info"
}

# Calculate optimal context length based on VRAM
calculate_context_length() {
    local vram_mb=$1
    
    # Rough estimates for context length based on available VRAM
    # These are conservative estimates for 4-bit quantized models
    if [ $vram_mb -ge 24000 ]; then
        echo "32768"  # 24GB+ - Large context
    elif [ $vram_mb -ge 16000 ]; then
        echo "16384"  # 16GB - Medium-large context
    elif [ $vram_mb -ge 12000 ]; then
        echo "8192"   # 12GB - Medium context
    elif [ $vram_mb -ge 8000 ]; then
        echo "4096"   # 8GB - Small-medium context
    elif [ $vram_mb -ge 4000 ]; then
        echo "2048"   # 4GB - Small context
    else
        echo "1024"   # CPU/Minimal - Minimal context
    fi
}

# Generate Ollama environment configuration
generate_ollama_env() {
    local gpu_data=$1
    local gpu_type=$(echo "$gpu_data" | cut -d':' -f1)
    local vram_mb=$(echo "$gpu_data" | cut -d':' -f2)
    
    local env_file="/etc/ollama/environment"
    mkdir -p "$(dirname "$env_file")"
    
    cat > "$env_file" << EOF
# Ollama Environment Configuration
# Generated by LifeOS on $(date -Iseconds)

# Data directory
OLLAMA_MODELS=$OLLAMA_DATA_DIR/models
OLLAMA_HOST=0.0.0.0:11434
OLLAMA_ORIGINS=*

# GPU Configuration
EOF

    case "$gpu_type" in
        nvidia)
            local context_len=$(calculate_context_length $vram_mb)
            cat >> "$env_file" << EOF
CUDA_VISIBLE_DEVICES=0
OLLAMA_NUM_PARALLEL=1
OLLAMA_MAX_LOADED_MODELS=1
OLLAMA_CONTEXT_LENGTH=$context_len
EOF
            log_info "NVIDIA GPU configured with context length: $context_len"
            ;;
        amd)
            cat >> "$env_file" << EOF
HSA_OVERRIDE_GFX_VERSION=10.3.0
OLLAMA_NUM_PARALLEL=1
OLLAMA_MAX_LOADED_MODELS=1
EOF
            log_info "AMD GPU configured with ROCm support"
            ;;
        intel)
            cat >> "$env_file" << EOF
OLLAMA_INTEL_GPU=true
OLLAMA_NUM_PARALLEL=1
EOF
            log_info "Intel GPU configured"
            ;;
        *)
            cat >> "$env_file" << EOF
OLLAMA_NO_GPU=1
OLLAMA_NUM_PARALLEL=1
EOF
            log_info "CPU-only mode configured"
            ;;
    esac
    
    chmod 644 "$env_file"
    log_success "Environment configuration written to $env_file"
}

# Download and verify Ollama binary
download_ollama() {
    local arch=$(detect_arch)
    local download_url="https://github.com/ollama/ollama/releases/download/v${OLLAMA_VERSION}/ollama-linux-${arch}"
    local temp_file="/tmp/ollama-${OLLAMA_VERSION}"
    
    log_info "Downloading Ollama v${OLLAMA_VERSION} for ${arch}..."
    
    # Download with retry logic
    local retries=3
    for i in $(seq 1 $retries); do
        if curl -fsSL --retry 3 --connect-timeout 30 -o "$temp_file" "$download_url"; then
            break
        fi
        log_warn "Download attempt $i failed, retrying..."
        sleep 2
    done
    
    if [ ! -f "$temp_file" ]; then
        log_error "Failed to download Ollama after $retries attempts"
        exit 1
    fi
    
    log_success "Download complete"
    
    # Note: In production, verify SHA256 checksum here
    # For now, we skip verification but print the checksum for reference
    local actual_sha256=$(sha256sum "$temp_file" | cut -d' ' -f1)
    log_info "SHA256: $actual_sha256"
    
    # Make executable
    chmod +x "$temp_file"
    
    echo "$temp_file"
}

# Install Ollama binary
install_ollama_binary() {
    local binary_path=$1
    
    log_info "Installing Ollama to $OLLAMA_INSTALL_DIR..."
    
    install -Dm 755 "$binary_path" "$OLLAMA_INSTALL_DIR/ollama"
    rm -f "$binary_path"
    
    # Create symlink for convenience
    ln -sf "$OLLAMA_INSTALL_DIR/ollama" /usr/bin/ollama
    
    log_success "Ollama installed to $OLLAMA_INSTALL_DIR/ollama"
}

# Create ollama user and directories
setup_user_and_dirs() {
    log_info "Setting up ollama user and directories..."
    
    # Create user if doesn't exist
    if ! id "$OLLAMA_USER" &>/dev/null; then
        useradd -r -s /bin/false -m -d "$OLLAMA_DATA_DIR" -c "Ollama AI Service" "$OLLAMA_USER"
        log_success "Created user: $OLLAMA_USER"
    else
        log_info "User $OLLAMA_USER already exists"
    fi
    
    # Create directories
    mkdir -p "$OLLAMA_DATA_DIR/models"
    mkdir -p /etc/ollama
    
    # Set permissions
    chown -R "$OLLAMA_USER:$OLLAMA_USER" "$OLLAMA_DATA_DIR"
    chmod 755 "$OLLAMA_DATA_DIR"
    
    log_success "Directories created and permissions set"
}

# Create systemd service file
create_systemd_service() {
    log_info "Creating systemd service..."
    
    cat > /etc/systemd/system/ollama.service << 'EOF'
[Unit]
Description=Ollama AI Service
Documentation=https://github.com/ollama/ollama
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
User=ollama
Group=ollama
Environment="PATH=/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin"
EnvironmentFile=-/etc/ollama/environment
ExecStart=/usr/local/bin/ollama serve
ExecReload=/bin/kill -HUP $MAINPID
Restart=always
RestartSec=3
StartLimitInterval=60
StartLimitBurst=3

# Security hardening
NoNewPrivileges=true
ProtectSystem=strict
ProtectHome=true
ReadWritePaths=/var/lib/ollama
ProtectKernelTunables=true
ProtectKernelModules=true
ProtectControlGroups=true
RestrictSUIDSGID=true
RestrictRealtime=true
RestrictNamespaces=true
LockPersonality=true
MemoryDenyWriteExecute=true
SystemCallFilter=@system-service
SystemCallErrorNumber=EPERM

# Resource limits
LimitNOFILE=65536
LimitNPROC=4096

[Install]
WantedBy=multi-user.target
EOF

    chmod 644 /etc/systemd/system/ollama.service
    
    # Create socket activation file for faster startup
    cat > /etc/systemd/system/ollama.socket << 'EOF'
[Unit]
Description=Ollama AI Service Socket
Documentation=https://github.com/ollama/ollama

[Socket]
ListenStream=0.0.0.0:11434
Accept=false

[Install]
WantedBy=sockets.target
EOF

    chmod 644 /etc/systemd/system/ollama.socket
    
    systemctl daemon-reload
    log_success "Systemd service created"
}

# Enable and start Ollama service
enable_service() {
    log_info "Enabling Ollama service..."
    systemctl enable ollama.service
    systemctl enable ollama.socket 2>/dev/null || true
    log_success "Ollama service enabled"
}

# Pull default models
pull_default_models() {
    log_info "Pulling default models..."
    
    # Start service temporarily for model download
    systemctl start ollama.service
    
    # Wait for service to be ready
    local retries=30
    for i in $(seq 1 $retries); do
        if curl -fsSL http://localhost:11434/api/tags &>/dev/null; then
            break
        fi
        sleep 1
    done
    
    for model in "${DEFAULT_MODELS[@]}"; do
        log_info "Pulling model: $model"
        if su -s /bin/bash "$OLLAMA_USER" -c "$OLLAMA_INSTALL_DIR/ollama pull $model" 2>&1; then
            log_success "Model $model pulled successfully"
        else
            log_warn "Failed to pull model $model (will retry on next boot)"
        fi
    done
    
    # Stop service - it will be started properly on next boot
    systemctl stop ollama.service
}

# Create health check script
create_health_check() {
    log_info "Creating health check script..."
    
    cat > /usr/local/bin/ollama-health-check.sh << 'EOF'
#!/bin/bash
# Ollama health check script

set -e

OLLAMA_URL="${OLLAMA_URL:-http://localhost:11434}"
TIMEOUT=5

# Check if service is responding
if ! curl -fsSL --max-time $TIMEOUT "${OLLAMA_URL}/api/tags" > /dev/null 2>&1; then
    echo "ERROR: Ollama service not responding"
    exit 1
fi

# Check version
VERSION=$(curl -fsSL --max-time $TIMEOUT "${OLLAMA_URL}/api/version" 2>/dev/null | jq -r '.version' 2>/dev/null || echo "unknown")
echo "OK: Ollama v${VERSION} is running"
exit 0
EOF

    chmod 755 /usr/local/bin/ollama-health-check.sh
    log_success "Health check script created"
}

# Main installation function
main() {
    log_info "Starting Ollama installation (v${OLLAMA_VERSION})..."
    
    # Check if already installed
    if [ -f "$OLLAMA_INSTALL_DIR/ollama" ]; then
        local installed_version=$($OLLAMA_INSTALL_DIR/ollama --version 2>/dev/null | head -1 || echo "unknown")
        log_warn "Ollama already installed: $installed_version"
        log_info "Use --force to reinstall, or run 'ollama-install.sh upgrade' to upgrade"
        
        if [ "${1:-}" != "--force" ] && [ "${1:-}" != "upgrade" ]; then
            exit 0
        fi
    fi
    
    # Detect GPU
    log_info "Detecting GPU..."
    local gpu_data=$(detect_gpu)
    
    # Download Ollama
    local binary_path=$(download_ollama)
    
    # Setup user and directories
    setup_user_and_dirs
    
    # Install binary
    install_ollama_binary "$binary_path"
    
    # Generate environment config based on GPU
    generate_ollama_env "$gpu_data"
    
    # Create systemd service
    create_systemd_service
    
    # Create health check
    create_health_check
    
    # Enable service
    enable_service
    
    # Pull default models
    pull_default_models
    
    log_success "Ollama installation complete!"
    log_info "Service will start automatically on next boot"
    log_info "To start now: systemctl start ollama"
    log_info "To check status: systemctl status ollama"
    log_info "To chat: ollama run qwen3:8b"
}

# Handle upgrade command
upgrade() {
    log_info "Upgrading Ollama..."
    systemctl stop ollama.service 2>/dev/null || true
    main --force
}

# Show help
show_help() {
    cat << EOF
Ollama Installation Script for LifeOS

Usage: $0 [COMMAND] [OPTIONS]

Commands:
    install     Install Ollama (default)
    upgrade     Upgrade existing Ollama installation
    gpu-info    Show GPU detection info only
    help        Show this help message

Options:
    --force     Force reinstallation even if already installed

Examples:
    $0                      # Install Ollama
    $0 upgrade              # Upgrade Ollama
    $0 --force              # Force reinstall
    $0 gpu-info             # Show GPU info only
EOF
}

# Parse arguments
case "${1:-install}" in
    install|--force)
        main "$@"
        ;;
    upgrade)
        upgrade
        ;;
    gpu-info)
        detect_gpu
        ;;
    help|--help|-h)
        show_help
        ;;
    *)
        log_error "Unknown command: $1"
        show_help
        exit 1
        ;;
esac
