#!/bin/bash
# Ollama installation script for LifeOS
# Version: 0.5.1
# Downloads and installs Ollama with SHA256 verification, GPU detection, and model setup

set -euo pipefail

# Configuration
OLLAMA_VERSION="0.5.1"
OLLAMA_INSTALL_DIR="/usr/local/bin"
OLLAMA_DATA_DIR="/var/lib/ollama"
OLLAMA_USER="ollama"
DEFAULT_MODELS=("qwen3:8b" "llama3.2:3b")

